#Variables for a car

brand = 'Tesla'
model = 'Model S'
year = 2022
is_electric = True
colors_available = ['black', 'white', 'blue', 'red']
car_info = {
'brand': 'Tesla',
'model': 'Model S',
'year': 2022,
'is_electric': True,
'colors_available': ['black', 'white', 'blue', 'red']
}

#Printing the values stored in the variables

print('Brand:', brand)
print('Model:', model)
print('Year:', year)
print('Electric:', is_electric)
print('Colors Available:', colors_available)
print('Car information:', car_info)

